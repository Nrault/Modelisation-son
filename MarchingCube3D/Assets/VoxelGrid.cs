        int cellType = 0;
        // On vérifie l'état de chaque sommet qu'on a associé à une valeur binaire et on effectue un OU pour obtenir une valeur représentant
        // la configuration de la surface à l'intérieur du cube considéré
        if (cube.voxels[0].state)
        {
            cellType |= 1;
        }
        if (cube.voxels[1].state)
        {
            cellType |= 2;
        }
        if (cube.voxels[2].state)
        {
            cellType |= 4;
        }
        if (cube.voxels[3].state)
        {
            cellType |= 8;
        }
        if (cube.voxels[4].state)
        {
            cellType |= 16;
        }
        if (cube.voxels[5].state)
        {
            cellType |= 32;
        }
        if (cube.voxels[6].state)
        {
            cellType |= 64;
        }
        if (cube.voxels[7].state)
        {
            cellType |= 128;
        }
        if(cellType != 0)
        {
            
            for (int i = 0; i < 5; i++)
            {
                Vector3[] vect = new Vector3[3];
                int idx = vertices.Count;
                for (int j = 0; j < 3; j++)
                {
                    //On cherche le numéro de l'arrête du cube considéré à ajouter à notre liste de vertex
                    int vert = triangleConnectionTable[cellType, 3 * i + j];
                    if (vert < 0)
                    {
                        break;
                    }
                    vertices.Add(cube.edges[vert]);
                    triangles.Add(idx + windingOrder[j]);
                    vect[j] = cube.edges[vert];
                }
            }
        }
        
    }

