    // Fonction dessinant un cylindre
    // Entrées : Un Vector3 représentant le centre de la figure et le rayon du cylindre
    public void DrawCylinder(Vector3 centre, int ray)
    {
        int ray2 = ray * ray;
        int xMax = (int)(centre.x) + ray;
        int yMax = (int)(centre.y) + ray;
        int zMax = (int)(centre.z) + ray;
        // On commence par parcourir les cases de la grilles que nous avons créé, ici, nous limitons le nombre d'itérations en commençant 
        // à partir de la première valeur contenue dans le cylindre 
        for (int x = (int)(centre.x) - ray; x < resolution && x <= xMax; x++)
        {
            for (int y = (int)(centre.y) - ray; y < resolution && y <= yMax + ray; y++)
            {
                for (int z = (int)(centre.z) - ray; z < resolution && z <= zMax; z++){
                    // On utilise le théorème de Pythagore afin de savoir si le point se situe dans la base du cylindre
                    if ((centre.x - x) * (centre.x - x) + (centre.y - y) * (centre.y - y) <= ray2)
                    {
                        // Si c'est le cas, on set le voxel à true pour dire que cette case contient un morceau de la figure
                            voxels[z * resolution * resolution + y * resolution + x].state = true; 
                    }
                }             
            }
        }
        Refresh();
    }

    public void DrawOvaloid(Vector3 centre, int a, int b, int c)
    {
        for (int x = (int)(centre.x) - a; x < resolution && x <= (int)(centre.x) + a; x++)
        {
            for (int y = (int)(centre.y) - b; y < resolution && y <= (int)(centre.y) + b; y++)
            {
                for (int z = (int)(centre.z) - c; z < resolution && z <= (int)(centre.z) + c; z++)
                {
                    // On vérifie que le voxel considéré face partie de l'ovaloid
                    if (((centre.x - x) * (centre.x - x))/a + ((centre.y - y) * (centre.y - y))/b + ((centre.z - z) * (centre.z - z))/c <= 1)
                    {
                        voxels[z * resolution * resolution + y * resolution + x].state = true;
                    }
                }
            }
        }
        Refresh();
    }

    // Fonction permettant de dessiner une sphère
    // Entrée : Le centre de la sphère et son rayon
    public void DrawSphere(Vector3 centre, int ray)
    {
        int ray2 = ray * ray;
        int xMax = (int)(centre.x) + ray;
        int yMax = (int)(centre.y) + ray;
        int zMax = (int)(centre.z) + ray;
        float centreX2 = 0;
        float centreY2 = 0;
        float centreZ2 = 0;
        // Comme pour le cylindre, nous commençons par parcourir la grille en commençant par la plus petite valeur contenue dans la sphère
        // afin de limiter les itérations
        for (int x = (int)(centre.x) - ray; x < resolution && x <= xMax; x++)
        {
            centreX2 = (centre.x - x) * (centre.x - x);
            for (int y = (int)(centre.y) - ray; y < resolution && y <= yMax; y++)
            {
                centreY2 = (centre.y - y) * (centre.y - y);
                for (int z = (int)(centre.z) - ray; z < resolution && z <= zMax; z++)
                {
                    centreZ2 = (centre.z - z) * (centre.z - z);
                    // Si la norme du vecteur, obtenu en soustrayant le point considéré au centre de notre sphère, est inférieur au carré du rayon
                    // alors le point est contenu dans la sphère
                    if ((centreX2 + centreY2 + centreZ2) - ray2 <= 0)
                    {
                        // On set donc la case correspondante dans le tableau de booléen à true
                            voxels[z * resolution * resolution + y * resolution + x].state = true;
                    }
                }
            }
        }
        Refresh();
    }


    /********************************************************Marching Cube*******************************************************************/
    private void Triangulate()
    {
        mf.mesh = mesh;
        vertices.Clear();
        triangles.Clear();
        mesh.Clear();

        TriangulateCellRows();

        mesh.vertices = vertices.ToArray();
        mesh.triangles = triangles.ToArray();
    }

    private void TriangulateCellRows()
    {
        int cells = resolution - 1;
        int i = 0;
        while (i < resolution * resolution * resolution - resolution * resolution - resolution - 1)
        {
            // On triangule la surface à l'intérieur du cube considéré
            TriangulateCell(cubes[i]);
            i++;
        }
    }

    private void TriangulateCell(Cube cube)
